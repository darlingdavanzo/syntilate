<div class="row">
	<div class="col-md">
		<hr/>
		<h2>1) Dall'idea alla soluzione software (0-2 giorni)</h2>
		<br>
		<p>
			Tu ci racconti la tua idea e noi ti proponiamo le soluzioni.<br>
			Puoi scegliere di stabilire autonomamente i requisiti o chiederci una prima consulenza gratuita.<br>
			Ti aiuteremo a concretizzare la tua idea di business in una lista di requisiti tecnici, potrai scegliere le feature da includere nel tuo prodotto e chiedere un preventivo gratuito.<br>
			La parola chiave è flessibilità, questo workflow non è un vincolo ma un principio, siamo aperti a soluzioni diverse e anche ad estensioni del tuo team.
		</p>
	</div>
	<div class="col-md">
		<hr/>
		<h2>2) Pianificazione dei lavori (0-7 giorni)</h2>
		<br>
		<p>
			A seconda della complessità del lavoro decideremo se dividerlo in blocchi o meno. Ogni blocco avrà un suo specifico set di funzionalità.<br>
			Il caso più semplice consiste in due blocchi: il primo è un prototipo o Minimum Viable Product, il secondo comprende l'integrazione di tutte le funzionalità e il design.<br>
			Il contratto prevede un pagamento anticipato e un pagamento alla fine di ogni blocco.
		</p>
	</div>
</div>
<div class="row">
	<div class="col-md">
		<hr/>
		<h2>3) Sviluppo del prodotto (7-30 giorni a blocco)</h2>
		<br>
		<p>
			Ci piace lavorare usando una metodologia agile, a stretto contatto con il cliente.<br>
			Chiediamo continuo feedback sul lavoro e accettiamo variazioni dei requisiti per i blocchi successivi.<br>
			Usiamo una variante nostra della metodologia Scrum, che include principi delle metodologie Lean e Kanban.
		</p>
	</div>
	<div class="col-md">
		<hr/>
		<h2>4) Conclusione dei lavori e rapporti futuri (0-99 anni)</h2>
		<br>
		<p>
			A conclusione del lavoro, puoi scegliere di accettare la soluzione chiavi in mano oppure firmare un contratto che a prezzi molto competitivi include la manutenzione continuativa dell'applicazione e l'aggiornamento delle feature.
		</p>
	</div>
